    private static final Map<String, AdifCountry> nameToCountryMap;
    private static final Map<Integer, AdifCountry> numberToCountryMap;
    private static final List<AdifCountry> sortedAdifCountriesList;
    private final int number;
    private final String name;
    private final boolean deleted;

    static {
        nameToCountryMap = new TreeMap<>();
        numberToCountryMap = new LinkedHashMap<>();
        sortedAdifCountriesList = new ArrayList<>();
        List<String> countryNamesList = new ArrayList<>();
        for (AdifCountry adifCountry : AdifCountry.values()) {
            numberToCountryMap.put(adifCountry.number, adifCountry);
            nameToCountryMap.put(adifCountry.name, adifCountry);
            countryNamesList.add(adifCountry.name);
        }
        Collections.sort(countryNamesList);
        for (String name : countryNamesList) {
            sortedAdifCountriesList.add(nameToCountryMap.get(name));
        }
    }

    AdifCountry(int number, String name) {
        this.number = number;
        this.name = name;
        this.deleted = false;
    }

    AdifCountry(int number, String name, boolean deleted) {
        this.number = number;
        this.name = name;
        this.deleted = deleted;
    }

    public final int getNumber() {
        return number;
    }

    public final String getName() {
        return name;
    }

    @SuppressLint("unused")
    public final boolean getDeleted() {
        return deleted;
    }

    public static AdifCountry getAdifCountryByName(String name) {
        return nameToCountryMap.get(name);
    }

    public static AdifCountry getAdifCountryByNumber(int number) {
        return numberToCountryMap.get(number);
    }

    public static Set<String> getAdifCountryNames() {
        return nameToCountryMap.keySet();
    }

    @SuppressLint("unused")
    public static List<AdifCountry> getAdifCountries() {
        return sortedAdifCountriesList;
    }

